/**
  * _isupper - checks for uppercase letters of the alphabet characters, c
  * @c: the letters of tge alphabet
  * Return: 1 if letter is uppercase and 0 otherwise
  */

int _isupper(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	else
		return (0);

	/* ASCII Code is used here */

}
