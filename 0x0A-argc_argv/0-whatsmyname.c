#include <stdio.h>

/**
 * main - Prints a program's with a new line.
 * @argc: The count of arguments supplied to the program.
 * @argv: An array of pointers to the arguments.
 * Return: Always o.
 */

int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
